public class multipliation {
    public static void main(String[]args){
        int n = 5;
        int i =1;
        {
            System.out.println("Multiplication of " + n +" is:");
        }
        while(i<=10){
            System.out.println(n+ " * " + i + "="+ (n*i)); 
            i++;   
        }
    }
}
